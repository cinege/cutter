    MyKeyListener(final JTextField source, final JTextField target, final JCheckBox mode) {
        this.source = source; 
        this.target = target;
        this.mode = mode;
    }
    
    @Override
    public void keyPressed(final KeyEvent arg0) {
    	String oldstring = "";
    	oldstring = this.source.getText();
        char newchar = arg0.getKeyChar();
        if (Character.isLetter(newchar)) {
       		String inputtext = String.valueOf(oldstring) + newchar;
            String result = this.getCutter(inputtext);
            StringSelection stringSelection = new StringSelection(result);
            Clipboard clpbrd = Toolkit.getDefaultToolkit().getSystemClipboard();
            clpbrd.setContents(stringSelection, null);
            this.target.setText(result);
	
        } else if (arg0.getKeyCode() == 86){
        	//ctrl+v
        	Clipboard clpbrd = Toolkit.getDefaultToolkit().getSystemClipboard();
        	String data = "";
        	try {
				data = (String) clpbrd.getData(DataFlavor.stringFlavor);
			} catch (UnsupportedFlavorException e) {
				e.printStackTrace();
			} catch (IOException e) {
				e.printStackTrace();
			}
        	this.source.setText("");
        	String result = this.getCutter(data);
        	this.target.setText(result);
        }
        else if (!IntStream.of(CHARS).anyMatch(x -> x == arg0.getKeyCode())) {arg0.consume();}
    }
    
    @Override
    public void keyReleased(final KeyEvent arg0) {
    }
    
    @Override
    public void keyTyped(final KeyEvent arg0) {
    }
    
    String getCutter(String sourcetext) {
    	sourcetext = this.mode.isSelected() ? pl_to_ascii(sourcetext) : sourcetext;
        String keyword = "";
        ArrayList<String> cutter = this.keys();
        Collator collator = Collator.getInstance(new Locale("hu", "HU"));
        int i = 0;
        for (int diff = 1; i < cutter.size() && diff > -1; diff = collator.compare(sourcetext.toLowerCase(), keyword.toLowerCase()), ++i) {
            keyword = cutter.get(i).split(",")[0];
        }
        i = i < 2 ? i = 2 : i ;
        String tempresult = cutter.get(i - 2).split(",")[1];
        String firstletter = cutter.get(i - 2).split(",")[0].substring(0, 1);
        String cuttercode = String.valueOf(firstletter) + " " + tempresult.substring(tempresult.length() - 2);
        return cuttercode;
    }
    
    String pl_to_ascii(String input) {

		for (int i = 0; i < UNICODE.length; i++) {input = input.replace(UNICODE[i], ASCII[i]);}
		return input;
	}
    
